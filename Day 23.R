# The next approach was to reduce the map to a graph and do a DFS to find the 
# maximum number of steps possible. To reduce the map to a graph I simply 
# iterated through every plot that was not a # character, adding those that had
# more than 2 connecting plots, ie the intersections. Working with just the 
# intersections has the benefit of there being only one path between each 
# intersection so there are a single number of steps between each intersection.
# With the intersections as vertices I iterated through the vertices looking for
# edges connecting it with other vertices and added those to the graph with the
# number of steps as weights.
#
# Solving the problem then became a question of doing a DFS from the start
# vertex to the end vertex looking for the maximum number of steps.
#
# Still not a fast solution, but it works and resolves in a reasonable amount 
# of time. Actually faster than Part One.

