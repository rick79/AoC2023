count_edges <- function(map, node, symbols = c(".", ">", "<", "^", "v")) {
  edges <- 0
  for(d in list(c(1, 0), c(-1, 0), c(0, 1), c(0, -1))) {
    npos <- node + d
    if(npos[1] >= 1 & npos[1] <= nrow(map) & npos[2] >= 1 & npos[2] <= ncol(map)) {
      if(map[npos[1], npos[2]] %in% symbols) edges <- edges + 1
    }
  }
  return(edges)
}

find_vertices <- function(map) {
  vertices <- list(c(1, which(map[1,] == ".") |> as.numeric()))
  pv <- which(map != "#", arr.ind = TRUE) |> unname() 
  for(i in 1:nrow(pv)) {
    if(count_edges(map, c(pv[i,1], pv[i, 2])) > 2) {
      vertices <- vertices |> append(list(c(pv[i,1] |> as.numeric(), pv[i, 2] |> as.numeric())))
    }
  }
  vertices <- vertices |> append(list(    c(nrow(map) |> as.numeric(), which(map[nrow(map),] == ".") |> as.numeric()  )))
  return(vertices )
}

find_edges <- function(map, start, vertices) {
  vertices <- setdiff(vertices, list( start ))
  edges <- list()
  expanded <- list()
  frontier <- queue()
  frontier$push(list(pos = start, weight = 0))
  while(frontier$size() > 0) {
    v <- frontier$pop()
    if(list(v$pos) %in% vertices) {
      edges <- edges |> append(list(list(from = start, to = v$pos, weight = v$weight)))
      next
    }
    expanded <- expanded |> append(list(v$pos))
    c <- map[v$pos[1], v$pos[2]]
    for(d in list(c(1, 0), c(-1, 0), c(0, 1), c(0, -1))) {
      npos <- v$pos + d
      if(npos[1] >= 1 & npos[1] <= nrow(map) & npos[2] >= 1 & npos[2] <= ncol(map)) {
        if(!(list(npos) %in% expanded) & map[npos[1], npos[2]] != "#") {
          frontier$push(list(pos = npos, weight = v$weight + 1))
        }
      }
    }
  }
  return(edges)
}

filter_edges <- function(edges, vertex) {
  filtered <- list()
  for(edge in edges) {
    if(all(edge$from == vertex)) {
      filtered <- filtered |> append(list(edge))
    }
  }
  return(filtered)
}

vertices <- find_vertices(map)
edges <- list()
for(vertex in vertices) {
  edges <- edges |> append(find_edges(map, vertex, vertices))
}

dfs <- function(graph, vertex, end, expanded = list()) {
  if(all(vertex == end)) return(0)
  length <- -Inf
  expanded <- expanded |> append(list(vertex))
  edges <- filter_edges(graph, vertex)
  for(edge in edges) {
    if(!(list(edge$to) %in% expanded)) {
      length <- max(length, dfs(graph, edge$to, end, expanded) + edge$weight)
    }
  }
  return(length)
}

